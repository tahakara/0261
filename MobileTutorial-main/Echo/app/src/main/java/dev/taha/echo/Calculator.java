package dev.taha.echo;

public class Calculator {
    public int topla(int s1, int s2) {
        return s1 + s2;
    }

    public int cikar(int s1, int s2) {
        return s1 - s2;
    }

    public int carp(int s1, int s2) {
        return s1 * s2;
    }

    public int bol(int s1, int s2) {
        return s1 / s2;
    }
}
